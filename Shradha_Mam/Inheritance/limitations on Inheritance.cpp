
// 🔷 Limitations of Inheritance
// 👉 Introduction
// Yes, inheritance is a powerful feature of OOP, but it also has some limitations and challenges.

// 👉 1. Increased Complexity
// When inheritance involves multiple levels (parent → child → grandchild), it becomes complex to understand and manage.

// 👉 2. Slower Execution
// Inheritance may take more processing time because the program may need to go through multiple classes to find the correct method.

// 👉 3. Tight Coupling
// The parent class and child class are strongly connected.

// 👉 This means:
// Changes in the parent class can affect the child class
// Maintenance becomes difficult

// 👉 4. Difficult to Modify
// If you make changes in the base class, you may also need to modify all derived classes, which increases effort.

// 👉 5. Risk of Errors
// If inheritance is not implemented properly, it can lead to:

// Unexpected behavior
// Incorrect outputs
// Hard-to-find bugs

// 👉 6. Not Always Suitable
// Inheritance is not suitable for every problem, especially when:

// Classes are not closely related
// Code reuse is not needed

// 🔥 Final Interview Line
// Although inheritance improves code reusability, it increases complexity, creates tight coupling between classes, and can lead to maintenance and performance issues if not used properly.