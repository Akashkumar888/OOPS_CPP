
// 🔷 Difference Between Abstract Class and Interface
// 👉 1. Methods

// An abstract class can have both abstract methods (without body) and normal methods (with implementation).
// An interface contains only abstract methods (no implementation).

// 👉 2. Variables

// An abstract class can have any type of variables (final, non-final, static, non-static).
// An interface usually contains only static and final variables (constants).

// 👉 3. Implementation

// An abstract class can provide partial implementation of methods.
// An interface provides no implementation, only method declarations.

// 👉 4. Inheritance

// An abstract class does not support multiple inheritance (a class can extend only one abstract class).
// An interface supports multiple inheritance (a class can implement multiple interfaces).

// 👉 5. Purpose

// An abstract class is used when classes are closely related and share common behavior.
// An interface is used to define a common contract for unrelated classes.

// 👉 6. Object Creation

// We cannot create objects of an abstract class directly.
// We cannot create objects of an interface either.

// 🔥 Final Interview Line
// An abstract class can have both implemented and unimplemented methods and supports single inheritance, whereas an interface contains only abstract methods and supports multiple inheritance, acting as a contract for classes.
