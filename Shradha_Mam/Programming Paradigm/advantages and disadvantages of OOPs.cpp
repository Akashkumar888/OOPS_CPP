
// 🔷 Advantages and Disadvantages of OOPs
// ✅ Advantages of OOPs

// 👉 1. Code Reusability
// OOP allows code reuse using concepts like inheritance, so the same code can be used again in different parts of the program.

// 👉 2. Easy Maintenance
// Programs written in OOP are easy to maintain and update because code is divided into small objects.

// 👉 3. Data Security
// OOP provides better data security through encapsulation, which restricts direct access to data.

// 👉 4. Easy to Modify and Extend
// OOP makes it easy to redesign and extend programs, reducing overall complexity.

// 👉 5. Better Structure for Large Programs
// OOP is very useful for large and complex software, as it keeps the code organized.

// ❌ Disadvantages of OOPs
// 👉 1. Requires Skilled Programmers
// OOP requires good understanding and thinking in terms of objects, which can be difficult for beginners.

// 👉 2. Needs Proper Planning
// OOP programs need proper design and planning, otherwise they can become complex.

// 👉 3. Not Suitable for All Problems
// OOP is not ideal for small or simple problems, where procedural programming may be better.

// 👉 4. Larger Program Size
// OOP programs are often longer and more complex compared to procedural programs.

// 🔥 Final Interview Line
// OOP provides reusability, security, and easy maintenance, but it requires good design skills, proper planning, and may increase program complexity.
