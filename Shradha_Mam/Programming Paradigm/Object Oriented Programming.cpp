
// 🔷 1. What is Object-Oriented Programming (OOP)?

// 👉 Simple Definition:
// Object-Oriented Programming (OOP) is a way of writing programs where everything is treated as an object.

// 👉 Easy Explanation:
// An object = data + functions (that work on that data)
// Objects interact with each other to perform tasks
// 💡 Example (Real Life)

// Think of a Car 🚗
// Data → color, speed
// Functions → drive(), brake()

// 👉 So, a car is an object.

// 💡 Interview Line (Best Answer)
// OOP is a programming paradigm where programs are built using objects that contain both data and methods, and these objects interact with each other.