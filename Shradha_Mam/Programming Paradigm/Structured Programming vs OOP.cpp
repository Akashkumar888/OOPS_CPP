
// 🔷 3. Structured Programming vs OOP
// Feature	OOP	                                     Structured Programming
// Approach	Bottom-up	                               Top-down
// Focus	Objects & Data	                           Functions
// Data Security	High (Encapsulation)	             Low
// Reusability	High	                               Moderate
// Execution	Dynamic	                               Sequential
// Maintainability	Easy	                           Difficult
// Example	Java, C++	                               C

// 💡 Key Interview Difference:
// 👉 Structured Programming:

// Focuses on functions
// No data protection

// 👉 OOP:
// Focuses on objects
// Provides data security


// Difference Between Structured Programming and Object-Oriented Programming (OOP)
// 👉 1. Basic Concept

// Object-Oriented Programming (OOP) is based on objects that contain both data and methods (behavior).
// Structured Programming is based on functions/procedures, where the program is divided into small modules.

// 👉 2. Approach

// OOP follows a bottom-up approach, where small objects are created first and then combined.
// Structured Programming follows a top-down approach, where the problem is divided step by step into smaller functions.

// 👉 3. Focus

// In OOP, data is more important and is tightly coupled with functions.
// In Structured Programming, functions (logic) are more important than data.

// 👉 4. Data Security

// OOP provides high data security using encapsulation (data hiding).
// Structured Programming provides low data security, as data can be accessed freely.

// 👉 5. Code Reusability

// OOP provides high code reusability using inheritance and polymorphism.
// Structured Programming provides limited reusability using functions and loops.

// 👉 6. Program Execution

// In OOP, methods are called dynamically based on object behavior.
// In Structured Programming, execution is sequential, step-by-step.

// 👉 7. Maintainability

// OOP is easy to modify and maintain, especially for large programs.
// Structured Programming is hard to modify, especially when the program grows large.

// 👉 8. Real-World Modeling

// OOP models real-world entities (like car, student, bank).
// Structured Programming does not directly represent real-world objects.

// 👉 9. Suitability

// OOP is best for large and complex applications.
// Structured Programming is better for small and simple programs.

// 🔥 Final Interview Line
// Structured Programming focuses on functions and follows a top-down approach, whereas Object-Oriented Programming focuses on objects, provides better security and reusability, and follows a bottom-up approach.